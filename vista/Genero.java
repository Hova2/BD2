package ar.edu.uade.tpbd2.vista;

import javax.enterprise.inject.Model;

/**
 *
 * @author iguglielmone
 */

@Model
public class Genero {
    
    private Integer id;
    private String nombre;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
}
